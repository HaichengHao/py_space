sum = 0 #初始变量
a = 0#初始变量
while a<101 :#条件判断
    if a%2 ==0 : #判断a%2取余数是否为0，若是0则为偶数，反之则为奇数，这里也可以写为  if a%2 : //因为a已经赋值为0，为false
        sum+=a
    a+=1#改变变量
print('1-100偶数的累加和为:',sum)#2550


# 习题变种，计算1-100之间奇数的累加和
sum1 = 0
b = 0
while b <101 :
    if b%2 != 0 :
        sum1+=b
    b+=1
print('1-100之间的奇数累加和为:',sum1)#2550